    void drawNodes() {
        for (BinaryNode node : nodes) {
            drawOval(String.valueOf(node.getData()), node.getX(), node.getY());
            if (node.getLeft() != null) {
                drawLeft(node, node.getLeft());
            }
            if (node.getRight() != null) {
                drawRight(node, node.getRight());
            }
        }
    }

