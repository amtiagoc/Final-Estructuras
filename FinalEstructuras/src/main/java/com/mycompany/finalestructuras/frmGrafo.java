    private Color pickColor(int nextInt) {
        switch (nextInt) {
            case (0):
                return Color.red;
            case (1):
                return Color.ORANGE;
            case (2):
                return Color.YELLOW;
            case (3):
                return Color.GREEN;
            case (4):
                return Color.BLUE;
            case (5):
                return Color.magenta;
            case (6):
                return Color.CYAN;
            default:
                return Color.PINK;
        }
    }
